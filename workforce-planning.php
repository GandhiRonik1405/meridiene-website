            function animateCountUp(el, target, suffix = "", duration = 2000) {
                let start = 0;
                const increment = target / (duration / 16); // ~60fps
            
                function update() {
                start += increment;
                if (start < target) {
                    el.innerText = formatNumber(Math.floor(start)) + suffix;
                    requestAnimationFrame(update);
                } else {
                    el.innerText = formatNumber(target) + suffix;
                }
                }
            
                requestAnimationFrame(update);
            }
            
